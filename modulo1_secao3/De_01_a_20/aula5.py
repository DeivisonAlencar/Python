# Tipo bool -> Boolean
# Tipo de informção que só pode ter 2 opções
# True (verdadeiro) ou False (falso)
# É a resposta para uma pergunta feita ao programa através dos operadores

# Para confirmar se algo é igual a outra utilizamos o duplo '='
print(10 == 10) # Retorna True, pois 10 é igual a 10
print(10 != 5) # Retorna True, pois 10 é diferente de 5
print(10 > 20) # Retorna False, pois 10 não pe maior que 20
print( 10 < 5) # Retorna False, pois 10 não é menor que 5

