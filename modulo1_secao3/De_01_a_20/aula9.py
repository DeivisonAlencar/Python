adicao = 10 + 10 # soma de dois números (podendo ser int ou float)

subtracao = 10 - 5 # subtração de um número em outro (podendo ser int ou float)

multiplicacao = 10 * 10 # multiplicação e dois números (podendo ser int ou float)

divisao = 10 / 2.2 # divisão de um número por outro (podendo ser int ou float)

divisao_inteira = 10 // 2.2 # divisão de um número por outro onde se ignora as casas decimais (mesmo que um dos números seja float)

exponenciacao = 2 ** 10 # Elevação de um número por outro

modulo = 25 % 5 # Retorna o resto de uma divisão

print(adicao, subtracao, multiplicacao, divisao, divisao_inteira, exponenciacao, modulo, sep='\r\n')